# Print output
print("hello, world!")
"hello, world!"

# The print function just prints the value in the string, but returns nothing
result = print("hello, world!")
"hello, world"

print(result)
None

#We can convert None to a string and print it by using:
repr(result)
"None"

#We can also print multiple value, separated by space
print("Elian", "Braja", "Dev")
